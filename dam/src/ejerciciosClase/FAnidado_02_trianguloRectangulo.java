public class FAnidado_02_trianguloRectangulo {
    
    public static void main(String[] args) {
        
      
       
       int n = 5;
        
        for (int f = 0; f <= n; f++) {
           
           
            for (int c = f; c <= n; c++) {
                  System.out.print("*");   
          
            }
           
            System.out.println("");
          
          
        }
       
    }
    
}
